//Relational or Comparison operators

//>  - more than
//<  - less than
//>= - greater than or equal to
//<= - less than or equal to

var result = 10 > 5
console.log(result)

//Equality operators
var x = 1
console.log(x=='1') //lose comparision, which is only the data comparison
console.log(x==='1') //strict comparison, wich will check Datatype + Value
console.log(x === 1) // true, which is same value and datatype as well